
// Task 4: Join and Split

// Goal: Convert arrays to strings and vice versa.

// 1. Create an array: ["I", "love", "JavaScript"]
// 2. Join it into a single string with spaces.
// 3. Then split the string back into an array.

let arr = ["I", "love", "JavaScript"]
console.log(arr);
let newArr = arr.join(" ")
console.log(newArr);
console.log(typeof newArr);
let backArr = newArr.split(" ");
console.log(backArr);
console.log(typeof backArr);







